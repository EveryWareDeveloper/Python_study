# if 문 문법
a, b = 10, 10
if a > b:
    print("조건이 참이면 실행")
    print("실행코드는 띄어쓰기로 구분")
    # if문의 코드블록은 띄어쓰기 된 곳까지
elif a == b:  # else if
    print("a, b가 같다")
else:  # if문의 조건이 거짓이면 코드 실행
    print("if의 조건이 거짓일 때 실행")
print("종료")


